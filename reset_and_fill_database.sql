CREATE TABLE IF NOT EXISTS AutoPartsStore.Suppliers (
	SupplierID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    Company VARCHAR(45) NOT NULL,
    ContactName VARCHAR(50),
    PrimaryPhone CHAR(10) NOT NULL,
    SecondaryPhone CHAR(10),
    Website VARCHAR(255),
    Address VARCHAR(100) NOT NULL,
    City VARCHAR(45) NOT NULL,
    State CHAR(2) NOT NULL,
    ZipCode CHAR(10) NOT NULL,
    Notes TEXT
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.Shippers (
	ShipperID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    Company VARCHAR(45) NOT NULL,
    ContactName VARCHAR(50),
    PrimaryPhone CHAR(10) NOT NULL,
    SecondaryPhone CHAR(10),
    Website VARCHAR(255),
    Address VARCHAR(100) NOT NULL,
    City VARCHAR(45) NOT NULL,
    State CHAR(2) NOT NULL,
    ZipCode CHAR(10) NOT NULL,
    Notes TEXT
);

CREATE TABLE AutoPartsStore.CarMakes (
	CarMakeID INT UNSIGNED PRIMARY KEY,
    MakeName VARCHAR(45) UNIQUE NOT NULL
);

CREATE TABLE AutoPartsStore.CarModels (
	CarModelID INT UNSIGNED PRIMARY KEY,
    CarMakeID INT UNSIGNED NOT NULL,
    CarModelName VARCHAR(45) UNIQUE NOT NULL,
    FOREIGN KEY (CarMakeID) REFERENCES AutoPartsStore.CarMakes(CarMakeID)
);

CREATE TABLE AutoPartsStore.CarConfigurations (
	CarID INT UNSIGNED PRIMARY KEY,
    CarMakeID INT UNSIGNED NOT NULL,
    CarModelID INT UNSIGNED NOT NULL,
	ProductionYear CHAR(4) NOT NULL,
    Notes TEXT,
    FOREIGN KEY (CarMakeID) REFERENCES AutoPartsStore.CarMakes(CarMakeID),
    FOREIGN KEY (CarModelID) REFERENCES AutoPartsStore.CarModels(CarModelID)
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.Parts (
	PartID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    SupplierID INT UNSIGNED NOT NULL,
    CarID INT UNSIGNED NOT NULL,
    Name VARCHAR(100) NOT NULL,
    Category VARCHAR(50),
    PartDescription TEXT,
    PricePerUnit DECIMAL(10,2) NOT NULL, /*Max 99,999,999.99*/
    Discontinued BOOLEAN NOT NULL DEFAULT FALSE,
    QuantityOnHand INT UNSIGNED NOT NULL DEFAULT 0,
    Image LONGBLOB DEFAULT NULL,
    FOREIGN KEY (SupplierID) REFERENCES AutoPartsStore.Suppliers(SupplierID),
    FOREIGN KEY (CarID) REFERENCES AutoPartsStore.CarConfigurations(CarID)
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.OrderStatus (
	OrderStatusID INT UNSIGNED PRIMARY KEY,
    Name VARCHAR(45) NOT NULL
);

