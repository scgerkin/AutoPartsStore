DROP SCHEMA IF EXISTS AutoPartsStore;
CREATE SCHEMA IF NOT EXISTS AutoPartsStore;

CREATE TABLE IF NOT EXISTS AutoPartsStore.Customers (
    CustomerID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    LastName VARCHAR(45) NOT NULL,
    FirstName VARCHAR(45) NOT NULL,
    Email VARCHAR(255) NOT NULL UNIQUE,
    PrimaryPhone VARCHAR(10) NOT NULL,
    SecondaryPhone VARCHAR(45) NULL,
    Address VARCHAR(100) NOT NULL,
    City VARCHAR(45) NOT NULL,
    State CHAR(2) NOT NULL,
    ZipCode CHAR(10) NOT NULL,
    Notes TEXT
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.Employees (
    EmployeeID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    LastName VARCHAR(45) NOT NULL,
    FirstName VARCHAR(45) NOT NULL,
    Title VARCHAR(30),
    Email VARCHAR(255) UNIQUE NOT NULL,
    PrimaryPhone VARCHAR(10) NOT NULL,
    SecondaryPhone VARCHAR(45) NULL,
    Address VARCHAR(100) NOT NULL,
    City VARCHAR(45) NOT NULL,
    State CHAR(2) NOT NULL,
    ZipCode CHAR(10) NOT NULL,
    Notes TEXT
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.PrivilegeDetails (
    PrivilegeID INT UNSIGNED PRIMARY KEY,
    Name VARCHAR(30) NOT NULL,
    Notes TEXT
);

