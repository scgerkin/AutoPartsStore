CREATE TABLE IF NOT EXISTS AutoPartsStore.CustomerOrders (
	OrderID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    CustomerID INT UNSIGNED NOT NULL,
    OrderDate TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ShipDate TIMESTAMP,
    ShipName VARCHAR(90) NOT NULL,
    ShipAddress VARCHAR(100) NOT NULL,
    ShipCity VARCHAR(45) NOT NULL,
    ShipState CHAR(2) NOT NULL,
    ShipZipCode CHAR(10) NOT NULL,
    ShipperID INT UNSIGNED,
    ShipFee DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    TaxStatusID INT UNSIGNED NOT NULL DEFAULT 0,
    TaxAmount DECIMAL(10,2) NOT NULL DEFAULT 0.00,
    OrderStatusID INT UNSIGNED NOT NULL DEFAULT 0,
    Notes TEXT,
    FOREIGN KEY (CustomerID) REFERENCES AutoPartsStore.Customers(CustomerID),
    FOREIGN KEY (ShipperID) REFERENCES AutoPartsStore.Shippers(ShipperID),
    FOREIGN KEY (TaxStatusID) REFERENCES AutoPartsStore.TaxStatus(TaxStatusID),
    FOREIGN KEY (OrderStatusID) REFERENCES AutoPartsStore.OrderStatus(OrderStatusID)
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.ResupplyOrders (
	ResupplyOrderID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    CreatedBy INT UNSIGNED NOT NULL,
    CreationDate TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ShipFee DECIMAL(10, 2) NOT NULL,
    ShipDate TIMESTAMP,
    TaxAmount DECIMAL(10, 2) NOT NULL DEFAULT 0.00,
    OrderStatusID INT UNSIGNED NOT NULL DEFAULT 0,
    ApprovedBy INT UNSIGNED DEFAULT NULL,
    ApprovedDate TIMESTAMP,
    Notes TEXT,
    FOREIGN KEY (CreatedBy) REFERENCES AutoPartsStore.Employees(EmployeeID),
    FOREIGN KEY (OrderStatusID) REFERENCES AutoPartsStore.OrderStatus(OrderStatusID),
    FOREIGN KEY (ApprovedBy) REFERENCES AutoPartsStore.Employees(EmployeeID)
);

CREATE TABLE IF NOT EXISTS AutoPartsStore.OrderDetails (
	OrderDetailsID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
    CustomerOrderID INT UNSIGNED,
    ResupplyOrderID INT UNSIGNED,
    PartID INT UNSIGNED NOT NULL,
    Quantity INT UNSIGNED NOT NULL DEFAULT 1,
    UnitPrice DECIMAL(10,2) NOT NULL,
    FOREIGN KEY (CustomerOrderID) REFERENCES AutoPartsStore.CustomerOrders(OrderID),
    FOREIGN KEY (ResupplyOrderID) REFERENCES AutoPartsStore.ResupplyOrders(ResupplyOrderID),
    FOREIGN KEY (PartID) REFERENCES AutoPartsStore.Parts(PartID)
);

